// Initialize
let index = 0;
// While loop to print values from 0 to 10 with a step of 2
while (index <= 10 /*condition*/) {
    console.log(`Value of index is ${index}`);
    // Increment index by 2
    index = index + 2;
}

let myArray = ['flash', "batman", "superman"];

// Initialize arr to 0
let arr = 0;
// While loop to iterate over the array
while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    // Increment arr to move to the next index
    arr = arr + 1;
}

// Initialize score to 11
let score = 11;
// Do-while loop to print score until it is less than or equal to 10
do {
    // Print the current value of score
    console.log(`Score is ${score}`);
    // Increment score
    score++;
} while (score <= 10);

//do-while => first do the task and then check condition

